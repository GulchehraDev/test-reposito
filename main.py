registered = []
def registration_student():
        name = input("введите имя: ")
        surname = input("введите фамилию: ")
        age = int(input("введите возраст: "))

        registered_person = {
            "name":name,
            "surname":surname,
            "age":age

        }
        registered.append(registered_person)

def show_student():
    for person in registered:
        print(person["name"],person["surname"], person["age"])


def delete_student():
    name = input("введите имя")
    for user in registered:
         if user["name"] == name:
            registered.remove(user)
            print("пользователь удалён")
            return


